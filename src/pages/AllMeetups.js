function AllMeetupsPage() {
  const [isLoading, setIsLoading] = useState(true);
  const [loadedMeetups, setLoadedMeetups] = useState([]);
  useEffect(() => {
    fetch(
      "https://react-getting-started-c574c-default-rtdb.firebaseio.com/meetups.json"
    )
      .then((response) => {
        // console.log(response.json());
        return response.json(); // use to conver json data into orignal data
      })
      .then((data) => {
        const storedMeetups = [];
        setIsLoading(false);
        for (let i in data) {
          const meetup = {
            id: i,
            ...data[i],
          };
          storedMeetups.push(meetup);
          // console.log(meetup);
        }
        setLoadedMeetups(storedMeetups);
      });
  }, []);
  // console.log(loadedMeetups);// Data in data base see on console
  if (isLoading) {
    return (
      <section>
        <p>Loading...</p>
      </section>
    );
  }
  return (
    <section>
      <h1>All Meetups</h1>
      <MeetupList meetups={loadedMeetups} />
    </section>
  );
}

export default AllMeetupsPage;
